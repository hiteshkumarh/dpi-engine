document.addEventListener('DOMContentLoaded', () => {
    const dropzone = document.getElementById('dropzone');
    const fileInput = document.getElementById('fileInput');
    const loadingState = document.getElementById('loadingState');
    const resultsPanel = document.getElementById('resultsPanel');
    const resetBtn = document.getElementById('resetBtn');

    // Counters
    const totalPacketsCounter = document.getElementById('totalPackets');
    const forwardedPacketsCounter = document.getElementById('forwardedPackets');
    const droppedPacketsCounter = document.getElementById('droppedPackets');

    // Drag events
    ['dragenter', 'dragover', 'dragleave', 'drop'].forEach(eventName => {
        dropzone.addEventListener(eventName, preventDefaults, false);
    });

    function preventDefaults(e) {
        e.preventDefault();
        e.stopPropagation();
    }

    ['dragenter', 'dragover'].forEach(eventName => {
        dropzone.addEventListener(eventName, highlight, false);
    });

    ['dragleave', 'drop'].forEach(eventName => {
        dropzone.addEventListener(eventName, unhighlight, false);
    });

    function highlight(e) {
        dropzone.classList.add('drag-active');
    }

    function unhighlight(e) {
        dropzone.classList.remove('drag-active');
    }

    // Handle Drop
    dropzone.addEventListener('drop', handleDrop, false);

    function handleDrop(e) {
        const dt = e.dataTransfer;
        const files = dt.files;
        handleFiles(files);
    }

    // Handle Click
    dropzone.addEventListener('click', () => {
        fileInput.click();
    });

    fileInput.addEventListener('change', function () {
        handleFiles(this.files);
    });

    function handleFiles(files) {
        if (files.length === 0) return;

        const file = files[0];

        if (!file.name.endsWith('.pcap')) {
            alert('Please upload a valid .pcap file');
            return;
        }

        uploadFile(file);
    }

    async function uploadFile(file) {
        // UI Transitions
        dropzone.style.display = 'none';
        document.querySelector('.rules-input-container').style.display = 'none';
        loadingState.style.display = 'block';
        resultsPanel.style.display = 'none';

        const formData = new FormData();
        formData.append('pcap', file);

        const rulesVal = document.getElementById('rulesInput').value;
        if (rulesVal) {
            formData.append('rules', rulesVal);
        }

        try {
            const response = await fetch('/api/analyze', {
                method: 'POST',
                body: formData
            });

            if (!response.ok) {
                const errorData = await response.json();
                throw new Error(errorData.error || 'Server error occurred');
            }

            const data = await response.json();

            // Transition UI
            loadingState.style.display = 'none';
            resultsPanel.style.display = 'block';

            // Animate Counters
            animateCounter(totalPacketsCounter, data.totalPackets);
            animateCounter(forwardedPacketsCounter, data.forwarded);
            animateCounter(droppedPacketsCounter, data.dropped);

            // Populate Terminal Output
            const rawOutputElem = document.getElementById('rawOutput');
            rawOutputElem.textContent = data.rawOutput || 'No output generated by DPI engine';

        } catch (error) {
            console.error('Upload failed:', error);
            alert('Error analyzing file: ' + error.message);

            // Reset UI
            loadingState.style.display = 'none';
            dropzone.style.display = 'flex';
        }
    }

    function animateCounter(element, target) {
        const duration = 1500; // ms
        const steps = 60;
        const stepTime = duration / steps;

        let current = 0;
        const increment = target / steps;

        element.textContent = '0';

        const timer = setInterval(() => {
            current += increment;

            if (current >= target) {
                element.textContent = target.toLocaleString();
                clearInterval(timer);
            } else {
                element.textContent = Math.floor(current).toLocaleString();
            }
        }, stepTime);
    }

    // Reset button
    resetBtn.addEventListener('click', () => {
        resultsPanel.style.display = 'none';
        dropzone.style.display = 'flex';
        document.querySelector('.rules-input-container').style.display = 'block';
        fileInput.value = ''; // Clear file input
    });
});
